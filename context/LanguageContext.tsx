interface LanguageContextType {
  currentLanguage: string;
  setLanguage: (languageCode: string) => void;
  t: (key: string, fallback?: string) => string;
  languages: Language[];
  getCurrentLanguage: () => Language;
}

const LanguageContext = createContext<LanguageContextType | null>(null);

const LANGUAGE_STORAGE_KEY = '@rigsnap_language';

export function LanguageProvider({ children }: { children: ReactNode }) {
  const { user, updateProfile } = useAuth();
  const [currentLanguage, setCurrentLanguage] = useState('en');

  useEffect(() => {
    if (user) {
      // Load language from user data
      setCurrentLanguage(user.language || 'en');
    } else {
      // No user, default to English
      setCurrentLanguage('en');
    }
  }, [user]);

  const setLanguage = async (languageCode: string) => {
    try {
      setCurrentLanguage(languageCode);

      // Save to local storage
      await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, languageCode);

      // If user is logged in, update database
      if (user) {
        await updateProfile({ language: languageCode });
      }
    } catch (error) {
      console.error('Failed to save language preference:', error);
    }
  };

  const t = (key: string, fallback?: string): string => {
    const translation =
      translations[currentLanguage]?.[key] ||
      translations['en']?.[key] ||
      fallback ||
      key;
    return translation;
  };

  const getCurrentLanguage = (): Language => {
    return (
      languages.find((lang) => lang.code === currentLanguage) || languages[0]
    );
  };

  return (
    <LanguageContext.Provider
      value={{
        currentLanguage,
        setLanguage,
        t,
        languages,
        getCurrentLanguage,
      }}
    >
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within LanguageProvider');
  }
  return context;
}
